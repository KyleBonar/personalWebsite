  <p>Creating a multidimensional array of events for the month, which is created from a MySQL database, and then comparing to the day of the month as the calendar is being built. Database queries are only executed for the visible month as to not bloat the page. Specific members are given access to forms which allow events to be created and removed, creating calendar that is always up to date. </p> 
